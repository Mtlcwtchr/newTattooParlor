package by.bsuir.tattooparlor.util.exception;

public class IllegalRolePresentedException extends UtilException {
    public IllegalRolePresentedException() {
        super();
    }

    public IllegalRolePresentedException(String message) {
        super(message);
    }

    public IllegalRolePresentedException(String message, Throwable cause) {
        super(message, cause);
    }

    public IllegalRolePresentedException(Throwable cause) {
        super(cause);
    }
}
